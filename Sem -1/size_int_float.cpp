#include<iostream>
using namespace std;
int main()
{
    int a;
    float b;
    double c;
    char d;
    cout<<"The size of int is : "<<sizeof(a)<<endl;
    cout<<"The size of char is : "<<sizeof(d)<<endl;
    cout<<"The size of float is : "<<sizeof(b)<<endl;
    cout<<"The size of double is : "<<sizeof(c)<<endl;
return 0;
}